import java.util.*;

public class PriorityQueue {
    public static void main(String[] args) {
        // Create an instance of PriorityQueue from java.util package
        PriorityQueue pq1 = new PriorityQueue();
         
        pq1.add(100);
        pq1.add(50);
        pq1.add(150);
        pq1.add(25);
        pq1.add(75);
        pq1.add(125);
        pq1.add(175);
        
        
        // Print the PriorityQueue
        System.out.println(pq1);
    }
}
